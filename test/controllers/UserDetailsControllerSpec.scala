package controllers

import connectors.FakeDataCacheConnector
import controllers.actions._
import models.{NormalMode, UkAddress}
import play.api.test.Helpers._
import utils.FakeNavigator
import views.html.userDetails

class UserDetailsControllerSpec extends ControllerSpecBase {

  def onwardRoute = routes.IndexController.onPageLoad()

  def controller(dataRetrievalAction: DataRetrievalAction = getEmptyCacheMap) =
    new UserDetailsController(frontendAppConfig, messagesApi, FakeDataCacheConnector, new FakeNavigator(desiredRoute = onwardRoute), FakeAuthAction,
      dataRetrievalAction, new DataRequiredActionImpl)

  val testAnswer = "answer"

  val testName = "firstName familyName"
  val testNino = "AB123456A"
  val testAddress = UkAddress("Line1", "Line2", Some("Line3"), Some("Line4"), Some("Line5"), "AB1 2CD")


  def viewAsString() = userDetails(frontendAppConfig, NormalMode, testName, testNino, testAddress)(fakeRequest, messages).toString

  "UserDetails Controller" must {

    "return OK and the correct view for a GET" in {
      val result = controller().onPageLoad(NormalMode)(fakeRequest)

      status(result) mustBe OK
      contentAsString(result) mustBe viewAsString()
    }

    "redirect to the next page on save and continue" in {
      val postRequest = fakeRequest.withFormUrlEncodedBody(("value", testAnswer))

      val result = controller().onSubmit(NormalMode)(postRequest)

      status(result) mustBe SEE_OTHER
      redirectLocation(result) mustBe Some(onwardRoute.url)
    }

  }
}
